    public void cargarTabla(java.util.List<modelo.Temporada> lista) {
        javax.swing.table.DefaultTableModel modelo
                = (javax.swing.table.DefaultTableModel) tblTemporadas.getModel();

        modelo.setRowCount(0);

        for (modelo.Temporada t : lista) {
            modelo.addRow(new Object[]{
                t.getId(),
                t.getNombre(),
                t.getFechaInicio(),
                t.getFechaFin(),
                t.getRecargo()
            });
        }
    }

    public modelo.Temporada getTemporadaSeleccionada() {

        int fila = tblTemporadas.getSelectedRow();
        if (fila == -1) {
            return null;
        }

        modelo.Temporada t = new modelo.Temporada();
        t.setId(Integer.parseInt(tblTemporadas.getValueAt(fila, 0).toString()));
        t.setNombre(tblTemporadas.getValueAt(fila, 1).toString());
        t.setFechaInicio(tblTemporadas.getValueAt(fila, 2).toString());
        t.setFechaFin(tblTemporadas.getValueAt(fila, 3).toString());
        t.setRecargo(Double.parseDouble(tblTemporadas.getValueAt(fila, 4).toString()));

        return t;
    }

    // ======================================================================
