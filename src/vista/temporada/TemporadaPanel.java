    public void actualizarTabla(java.util.List<modelo.Temporada> lista) {
        javax.swing.table.DefaultTableModel modelo
                = (javax.swing.table.DefaultTableModel) tblTemporadas.getModel();

        modelo.setRowCount(0); // Limpia la tabla

        for (modelo.Temporada t : lista) {
            modelo.addRow(new Object[]{
                t.getId(),
                t.getNombre(),
                t.getFechaInicio(),
                t.getFechaFin(),
                t.getRecargo() + "%"
            });
        }
    }

// ======================================================================
// Obtener ID seleccionado de la tabla
// ======================================================================
    public int getIdSeleccionado() {
        int fila = tblTemporadas.getSelectedRow();
        if (fila == -1) {
            return -1;
        }

        return Integer.parseInt(tblTemporadas.getValueAt(fila, 0).toString());
    }

